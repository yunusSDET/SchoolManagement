package homework;

public class Homework{
    String homeworkName;
    String homeworkContent;
    String homeworkStartingDate;
    String homeworkDeadline;

    public Homework(String homeworkName, String homeworkContent, String homeworkStartingDate, String homeworkDeadline) {
        this.homeworkName = homeworkName;
        this.homeworkContent = homeworkContent;
        this.homeworkStartingDate = homeworkStartingDate;
        this.homeworkDeadline = homeworkDeadline;
    }
}
